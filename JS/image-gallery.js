    // ======== STATE =========
    let currentCategory = "all";
    let currentSearch = "";
    let currentSort = "newest";
    let visibleCount = 9;
    const PAGE_SIZE = 6;
    let filteredList = [];
    let currentIndex = 0; // For lightbox

    // ======== HELPERS =========
    function filterPhotos() {
      let list = [...photos];

      if (currentCategory !== "all") {
        list = list.filter(p => p.category === currentCategory);
      }

      if (currentSearch.trim() !== "") {
        const q = currentSearch.toLowerCase();
        list = list.filter(p =>
          p.title.toLowerCase().includes(q) ||
          p.tags.some(t => t.toLowerCase().includes(q))
        );
      }

      if (currentSort === "newest") {
        list.sort((a, b) => new Date(b.date) - new Date(a.date));
      } else if (currentSort === "oldest") {
        list.sort((a, b) => new Date(a.date) - new Date(b.date));
      } else if (currentSort === "title") {
        list.sort((a, b) => a.title.localeCompare(b.title));
      }

      filteredList = list;
      updateResultsInfo();
    }

    function updateResultsInfo() {
      const info = document.getElementById("resultsInfo");
      if (filteredList.length === 0) {
        info.textContent = "No photos found. Try a different search or category.";
      } else {
        info.textContent = `${filteredList.length} photo(s) â€¢ Showing ${Math.min(visibleCount, filteredList.length)}`;
      }
    }

    function formatDate(dateStr) {
      const d = new Date(dateStr);
      return d.toLocaleDateString(undefined, { day: "2-digit", month: "short", year: "numeric" });
    }

    function humanCategory(cat) {
      switch (cat) {
        case "events": return "Events";
        case "campus": return "Campus Life";
        case "labs": return "Labs";
        case "sports": return "Sports";
        case "fests": return "Fests";
        default: return cat;
      }
    }

    // ======== RENDER GALLERY =========
    function renderGallery() {
      const grid = document.getElementById("galleryGrid");
      grid.innerHTML = "";

      const toShow = filteredList.slice(0, visibleCount);

      toShow.forEach((p, index) => {
        const card = document.createElement("article");
        card.className = "card";
        card.setAttribute("data-index", index.toString());

        card.innerHTML = `
          <div class="card-image">
            <img src="${p.src}" alt="${p.title}">
            <div class="card-badge">${humanCategory(p.category)}</div>
          </div>
          <div class="card-body">
            <div class="card-title">${p.title}</div>
            <div class="card-meta">
              <span>${formatDate(p.date)}</span>
              <span>${p.tags[0] || ""}</span>
            </div>
            <div class="tag-list">
              ${p.tags.map(t => `<span class="tag">${t}</span>`).join("")}
            </div>
          </div>
        `;

        card.addEventListener("click", () => openLightbox(index));
        grid.appendChild(card);
      });

      const loadMoreBtn = document.getElementById("loadMoreBtn");
      if (visibleCount >= filteredList.length) {
        loadMoreBtn.disabled = true;
        loadMoreBtn.textContent = "No more photos";
      } else {
        loadMoreBtn.disabled = false;
        loadMoreBtn.textContent = "Show more photos â†“";
      }

      updateResultsInfo();
    }

    // ======== LIGHTBOX =========
    function openLightbox(indexInFilteredList) {
      currentIndex = indexInFilteredList;
      const photo = filteredList[currentIndex];
      if (!photo) return;

      const backdrop = document.getElementById("lightboxBackdrop");
      backdrop.classList.add("active");

      document.getElementById("lightboxImage").src = photo.src;
      document.getElementById("lightboxTitle").textContent = photo.title;
      document.getElementById("lightboxCategory").textContent = humanCategory(photo.category);
      document.getElementById("lightboxDate").textContent = formatDate(photo.date);
      document.getElementById("lightboxDescription").textContent = photo.description;

      const tagsEl = document.getElementById("lightboxTags");
      tagsEl.innerHTML = photo.tags.map(t => `<span class="lightbox-tag">${t}</span>`).join("");

      document.getElementById("lightboxCounter").textContent =
        `${currentIndex + 1} / ${filteredList.length}`;
    }

    function closeLightbox() {
      document.getElementById("lightboxBackdrop").classList.remove("active");
    }

    function showNext(delta) {
      if (!filteredList.length) return;
      currentIndex = (currentIndex + delta + filteredList.length) % filteredList.length;
      openLightbox(currentIndex);
    }

    // ======== THEME =========
    function loadTheme() {
      const saved = localStorage.getItem("gallery-theme");
      if (saved === "dark") {
        document.body.classList.remove("light");
        document.body.classList.add("dark");
        document.getElementById("themeLabel").textContent = "Light";
        document.getElementById("themeToggle").firstElementChild.textContent = "â˜€ï¸";
      }
    }

    function toggleTheme() {
      const body = document.body;
      const label = document.getElementById("themeLabel");
      const iconSpan = document.getElementById("themeToggle").firstElementChild;

      if (body.classList.contains("dark")) {
        body.classList.remove("dark");
        body.classList.add("light");
        label.textContent = "Dark";
        iconSpan.textContent = "ðŸŒ™";
        localStorage.setItem("gallery-theme", "light");
      } else {
        body.classList.remove("light");
        body.classList.add("dark");
        label.textContent = "Light";
        iconSpan.textContent = "â˜€ï¸";
        localStorage.setItem("gallery-theme", "dark");
      }
    }

    // ======== INIT =========
    document.addEventListener("DOMContentLoaded", () => {
      document.getElementById("year").textContent = new Date().getFullYear().toString();

      // hero stats
      document.getElementById("heroTotalPhotos").textContent = photos.length.toString();
      const eventsCount = photos.filter(p => p.category === "events").length;
      document.getElementById("heroEventsCount").textContent = eventsCount.toString();
      const thisMonthCount = photos.filter(p => {
        const d = new Date(p.date);
        const now = new Date();
        return d.getMonth() === now.getMonth() && d.getFullYear() === now.getFullYear();
      }).length;
      document.getElementById("heroThisMonth").textContent = thisMonthCount.toString();

      loadTheme();

      // Category filters
      document.getElementById("categoryFilters").addEventListener("click", (e) => {
        const btn = e.target.closest(".filter-chip");
        if (!btn) return;
        [...document.querySelectorAll(".filter-chip")].forEach(b => b.classList.remove("active"));
        btn.classList.add("active");
        currentCategory = btn.dataset.category || "all";
        visibleCount = 9;
        filterPhotos();
        renderGallery();
      });

      // Search
      document.getElementById("searchInput").addEventListener("input", (e) => {
        currentSearch = e.target.value || "";
        visibleCount = 9;
        filterPhotos();
        renderGallery();
      });

      // Sort
      document.getElementById("sortSelect").addEventListener("change", (e) => {
        currentSort = e.target.value;
        visibleCount = 9;
        filterPhotos();
        renderGallery();
      });

      // Load more
      document.getElementById("loadMoreBtn").addEventListener("click", () => {
        visibleCount += PAGE_SIZE;
        renderGallery();
      });

      // Theme toggle
      document.getElementById("themeToggle").addEventListener("click", toggleTheme);

      // Lightbox events
      document.getElementById("lightboxClose").addEventListener("click", closeLightbox);
      document.getElementById("lightboxBackdrop").addEventListener("click", (e) => {
        if (e.target.id === "lightboxBackdrop") {
          closeLightbox();
        }
      });
      document.getElementById("prevBtn").addEventListener("click", () => showNext(-1));
      document.getElementById("nextBtn").addEventListener("click", () => showNext(1));

      // Keyboard navigation
      document.addEventListener("keydown", (e) => {
        const backdrop = document.getElementById("lightboxBackdrop");
        if (!backdrop.classList.contains("active")) return;

        if (e.key === "Escape") closeLightbox();
        else if (e.key === "ArrowRight") showNext(1);
        else if (e.key === "ArrowLeft") showNext(-1);
      });

      // Initial filter + render
      filterPhotos();
      renderGallery();
    });